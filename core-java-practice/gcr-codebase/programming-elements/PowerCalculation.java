import java.util.*;

public class PowerCalculation {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        System.out.print("base: ");
        int base = sc.nextInt();
        System.out.print("base: ");
        int expo = sc.nextInt();
        System.out.println(Math.pow(base, expo));
    }
}
